class Team():

    def __init__(self,name,players,coach):
        self.name = name
        self.players = players
        self.coach = coach

    def team_has_name(name):
        return name
    
    def team_has_players(players):
        return players

    def team_has_coach(coach):
        return coach    

    def coach_can_be_changed(coach):
        return coach    
